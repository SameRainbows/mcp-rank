const registryLink: SourceLink = {
  label: "Official MCP registry",
  type: "registry",
  url: "https://registry.modelcontextprotocol.io/",
};

type ServerSeed = Omit<McpServer, "description" | "evidenceUpdated" | "maintainerVerified" | "trustScore"> & {
  description?: string;
  evidenceUpdated?: string;
  maintainerVerified?: boolean;
};

function server(seed: ServerSeed): McpServer {
  return {
    ...seed,
    description: seed.description ?? seed.tagline,
    evidenceUpdated: seed.evidenceUpdated ?? seed.lastReviewed,
    maintainerVerified: seed.maintainerVerified ?? seed.status === "maintainer_verified",
    trustScore: overallScore(seed.score),
  };
}

const clients: ClientKey[] = ["claude", "cursor", "codex", "vscode"];

function score(base: number) {
  return {
    installDocs: base,
    maintenance: Math.max(0, base - 2),
    auth: Math.max(0, base - 8),
    compatibility: Math.min(100, base + 3),
    usefulness: Math.min(100, base + 5),
    safety: Math.max(0, base - 6),
  };
}

function repo(url: string): SourceLink {
  return { label: "GitHub repo", type: "repository", url };
}

function docs(url: string): SourceLink {
  return { label: "Official docs", type: "docs", url };
}

function npmPackage(name: string): SourceLink {
  return { label: "NPM package", type: "package", url: `https://www.npmjs.com/package/${name}` };
}

